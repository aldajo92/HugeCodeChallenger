public class Rates {

    private double GBP;
    private double EUR;
    private double JPY;
    private double BRL;

    /**
     * @return The GBP
     */
    public Double getGBP() { return GBP; }

    /**
     * @param GBP The GBP
     */
    public void setGBP(Double GBP) {
        this.GBP = GBP;
    }

    /**
     * @return The EUR
     */
    public Double getEUR() {
        return EUR;
    }

    /**
     * @param EUR The EUR
     */
    public void setEUR(Double EUR) {
        this.EUR = EUR;
    }

    /**
     * @return The JPY
     */
    public Double getJPY() {
        return JPY;
    }

    /**
     * @param JPY The JPY
     */
    public void setJPY(Double JPY) {
        this.JPY = JPY;
    }

    /**
     * @return The BRL
     */
    public Double getBRL() {
        return BRL;
    }

    /**
     * @param BRL The BRL
     */
    public void setBRL(Double BRL) {
        this.BRL = BRL;
    }
}
